function validateForm() {
    const isPhoneValid = phoneInput.value.replace(/\D/g, '').length === 12;
    const isEmailValid = emailInput.value.includes('@');
    const isAgeValid = dateOfBirthInput.value ? validateAge() : false;

    const isManual = document.querySelector('input[name="passwordMethod"]:checked').value === 'manual';
    const isPassValid = isManual ? (validatePassword(passwordInput.value) && passwordInput.value === passwordRepeatInput.value) : true;

    const isNameValid = document.getElementById('firstName').value.length >= 2 && document.getElementById('lastName').value.length >= 2;
    const isNickValid = nicknameInput.value.length > 0;
    const isAgreed = agreementCheckbox.checked;

    const isValid = isPhoneValid && isEmailValid && isAgeValid && isPassValid && isNameValid && isNickValid && isAgreed;
    submitBtn.disabled = !isValid;
    return isValid;
}

registerForm.addEventListener('input', validateForm);

registerForm.addEventListener('submit', async (e) => {
    e.preventDefault();
    const isManual = document.querySelector('input[name="passwordMethod"]:checked').value === 'manual';
    const finalPassword = isManual ? passwordInput.value : autoGeneratedPassword;

    const userData = {
        firstName: document.getElementById('firstName').value.trim(),
        lastName: document.getElementById('lastName').value.trim(),
        patronymic: document.getElementById('patronymic').value.trim(),
        phone: phoneInput.value.replace(/\D/g, ''),
        email: emailInput.value.trim(),
        nickname: nicknameInput.value.trim(),
        birthDate: dateOfBirthInput.value,
        password: await hashPassword(finalPassword),
        role: 'user',
        createdAt: new Date().toISOString()
    };

    try {
        const response = await fetch(`${API_BASE_URL}/users`, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(userData)
        });

        if (response.ok) {
            alert('Регистрация успешно завершена!');
            window.location.href = 'index.html';
        }
    } catch (err) {
        console.error('Ошибка регистрации:', err);
    }
});

loginForm.addEventListener('submit', async (e) => {
    e.preventDefault();
    const email = document.getElementById('loginEmail').value;
    const rawPass = document.getElementById('loginPassword').value;
    const hashedPass = await hashPassword(rawPass);

    try {
        const response = await fetch(`${API_BASE_URL}/users?email=${email}&password=${hashedPass}`);
        const users = await response.json();

        if (users.length > 0) {
            localStorage.setItem('currentUser', JSON.stringify(users[0]));
            window.location.href = 'booking.html';
        } else {
            alert('Неверный email или пароль');
        }
    } catch (err) {
        console.error('Ошибка входа:', err);
    }
});

