async function hashPassword(password) {
    const encoder = new TextEncoder();
    const data = encoder.encode(password);
    const hashBuffer = await crypto.subtle.digest('SHA-256', data);
    const hashArray = Array.from(new Uint8Array(hashBuffer));
    return hashArray.map(b => b.toString(16).padStart(2, '0')).join('');
}

function formatPhoneBy(value) {
    let digits = value.replace(/\D/g, '');
    if (digits.startsWith('375')) {
        digits = digits.slice(0, 12);
    } else if (digits.startsWith('80')) {
        digits = '375' + digits.slice(2, 11);
    } else if (digits.length > 0 && !digits.startsWith('375')) {
        digits = '375' + digits.slice(0, 9);
    }

    let formatted = '';
    if (digits.length > 0) formatted = '+375';
    if (digits.length > 3) formatted += ` (${digits.slice(3, 5)}`;
    if (digits.length > 5) formatted += `) ${digits.slice(5, 8)}`;
    if (digits.length > 8) formatted += `-${digits.slice(8, 10)}`;
    if (digits.length > 10) formatted += `-${digits.slice(10, 12)}`;
    return formatted;
}

function validatePassword(password) {
    const checks = {
        length: password.length >= 8 && password.length <= 20,
        uppercase: /[A-Z]/.test(password),
        lowercase: /[a-z]/.test(password),
        number: /[0-9]/.test(password),
        special: /[!@#$%^&*()_+\-=\[\]{}|;:,.<>?]/.test(password),
        notCommon: !TOP_100_PASSWORDS.includes(password.toLowerCase())
    };

    document.getElementById('req-length').className = 'req-item ' + (checks.length ? 'valid' : 'invalid');
    document.getElementById('req-uppercase').className = 'req-item ' + (checks.uppercase ? 'valid' : 'invalid');
    document.getElementById('req-lowercase').className = 'req-item ' + (checks.lowercase ? 'valid' : 'invalid');
    document.getElementById('req-number').className = 'req-item ' + (checks.number ? 'valid' : 'invalid');
    document.getElementById('req-special').className = 'req-item ' + (checks.special ? 'valid' : 'invalid');
    document.getElementById('req-common').className = 'req-item ' + (checks.notCommon ? 'valid' : 'invalid');

    return Object.values(checks).every(Boolean);
}

function validateAge() {
    const dob = new Date(dateOfBirthInput.value);
    const today = new Date();
    let age = today.getFullYear() - dob.getFullYear();
    const m = today.getMonth() - dob.getMonth();
    if (m < 0 || (m === 0 && today.getDate() < dob.getDate())) age--;

    const errorEl = document.getElementById('dateOfBirthError');
    if (age < 16) {
        errorEl.textContent = 'Регистрация разрешена только с 16 лет ';
        return false;
    }
    errorEl.textContent = '';
    return true;
}

document.getElementById('generateNicknameBtn').addEventListener('click', () => {
    const fName = document.getElementById('firstName').value.trim();
    const lName = document.getElementById('lastName').value.trim();

    if (!fName || !lName) {
        alert('Сначала введите Имя и Фамилию');
        return;
    }

    nicknameAttempts++;
    if (nicknameAttempts <= 5) {
        const suffix = NICKNAME_SUFFIXES[Math.floor(Math.random() * NICKNAME_SUFFIXES.length)];
        nicknameInput.value = (fName.slice(0, 2) + lName.slice(0, 3) + suffix + Math.floor(Math.random() * 99)).toLowerCase();
        document.getElementById('nicknameAttempts').textContent = `Попыток: ${nicknameAttempts}/5`;
    } else {
        nicknameInput.readOnly = false;
        nicknameInput.focus();
        document.getElementById('nicknameAttempts').textContent = 'Доступен ручной ввод';
    }
    validateForm();
});

